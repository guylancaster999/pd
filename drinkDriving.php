        <h2> Drunk In Charge</h2>
         This is where you are charged with being in charge of a car on a road or public place whilst unfit through drink or drugs.
         <br/><br/>
         There are defences available.
         You will have to show to the court that there was no likelihood of you driving the vehicle whilst unfit through drink or drugs.
         <br/><br/>
         The prosecution will have to prove to the court that there was a real risk of you driving.
         <br/><br/>
         This would appear to infringe the presumption of innocence, and put the defence under a burden of proof to prove his innocence.
         <br/><br/>
         The charge has no statutory definition and is a potentially wide concept.
         The owner of a vehicle is treated as being in charge until and unless he put the vehicle in someone else's charge or unless there was no realistic possibility of him resuming actual control of the car.
         For example where he is at home in bed, is a great distance from the car etc.
         <br/><br/>
